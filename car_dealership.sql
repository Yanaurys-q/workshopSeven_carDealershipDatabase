
DROP DATABASE IF EXISTS car_dealership;
CREATE DATABASE car_dealership;
USE car_dealership;

DROP TABLE IF EXISTS lease_contracts;
DROP TABLE IF EXISTS sales_contracts;
DROP TABLE IF EXISTS inventory;
DROP TABLE IF EXISTS vehicles;
DROP TABLE IF EXISTS dealerships;

CREATE TABLE dealerships (
    dealership_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    address VARCHAR(255),
    phone VARCHAR(20)
);

CREATE TABLE vehicles (
    VIN CHAR(17) PRIMARY KEY,
    make VARCHAR(50) NOT NULL,
    model VARCHAR(50) NOT NULL,
    year INT NOT NULL,
    color VARCHAR(30),
    price DECIMAL(10,2),
    sold BOOLEAN DEFAULT FALSE
);
CREATE INDEX idx_make_model_color ON vehicles(make, model, color);

CREATE TABLE inventory (
    dealership_id INT NOT NULL,
    VIN CHAR(17) NOT NULL,
    PRIMARY KEY (dealership_id, VIN),
    FOREIGN KEY (dealership_id) REFERENCES dealerships(dealership_id),
    FOREIGN KEY (VIN) REFERENCES vehicles(VIN)
);

CREATE TABLE sales_contracts (
    id INT AUTO_INCREMENT PRIMARY KEY,
    VIN CHAR(17) NOT NULL,
    dealership_id INT NOT NULL,
    customer_name VARCHAR(100),
    sale_date DATE,
    price DECIMAL(10,2),
    FOREIGN KEY (VIN) REFERENCES vehicles(VIN),
    FOREIGN KEY (dealership_id) REFERENCES dealerships(dealership_id)
);
CREATE INDEX idx_sales_dealer_date ON sales_contracts(dealership_id, sale_date);
CREATE INDEX idx_sales_vin ON sales_contracts(VIN);

CREATE TABLE lease_contracts (
    id INT AUTO_INCREMENT PRIMARY KEY,
    VIN CHAR(17) NOT NULL,
    customer_name VARCHAR(100),
    lease_term INT,
    monthly_payment DECIMAL(10,2),
    start_date DATE,
    end_date DATE,
    FOREIGN KEY (VIN) REFERENCES vehicles(VIN)
);
CREATE INDEX idx_lease_vin ON lease_contracts(VIN);

